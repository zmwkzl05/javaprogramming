package day02;

public class Ex_03 {

	public static void main(String[] args) {	// Static->static, main 앞에 void작성, string->String
		System.out.println ("이 프로그램은 많은 오류들을 가지고 있다, ");		// 문자열 시작할때 큰따옴표 빠짐
		System.out.println ("그러나 프로그램이 이 문장을 출력한다면 ")	;	// Out->out, 세미콜론이 빠짐
		System.out.println ("너는 모든 오류들을 고친 것이다."  );		// 문자열 끝에 큰따옴표가 빠짐

	}

}
